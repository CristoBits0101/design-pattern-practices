package com.patterns.creational.builder.models;

// Step 1: Create a class named vehicle
public class Vehicle {

    /**
     * Atributes
     */

    public String brand;
    public String type;
    public String engine;
    public int doors;

    /**
     * Getters
     */

    public String getBrand() {
        return brand;
    }

    public String getType() {
        return type;
    }

    public String getEngine() {
        return engine;
    }

    public int getDoors() {
        return doors;
    }

}
